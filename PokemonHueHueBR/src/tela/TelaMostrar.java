    public void lerImagem() {
        setItens();
        String nomeImagem = "src/pokemonsImagens/";
        nomeImagem += this.mostra.getIndece();
        nomeImagem += ".png";   // source.gif
        try {
            // Lendo um arquivo
            File sourceimage = new File(nomeImagem);
            image = ImageIO.read(sourceimage);

            // Lendo de um input stream
           /* InputStream is = new BufferedInputStream(
                    new FileInputStream(nomeImagem));
            image = ImageIO.read(is);*/

            // Lendo de uma URL
           /* URL url = new URL("http://java-tips.org/source.gif");
            image = ImageIO.read(url);*/
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel abrir a imagem.");
        }
        JLabel label = new JLabel(new ImageIcon(image));
        getContentPane().add(label, BorderLayout.CENTER);
        this.jImagem.getViewport().add(label);
    
    }

