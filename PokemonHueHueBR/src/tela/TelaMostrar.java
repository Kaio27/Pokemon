    public void setItens(){
        nome = "Nome: " + this.mostra.getNome();
        this.jTNome.setText(nome);
        indice = "Indice: " + this.mostra.getIndece();
        this.jTIndice.setText(indice);
        tipo = "Tipo: " + this.mostra.getTipo().toString();
        this.jTTipo.setText(tipo);
        x = "X: " + this.mostra.getCordenada().getX();
        this.jTX.setText(x);
        y = "Y: " + this.mostra.getCordenada().getY();
        this.jTY.setText(y);
        z = "Z: " + this.mostra.getCordenada().getZ();
        this.jTZ.setText(z);
    }
    
