    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if (this.jTextQuantidade != null) {
            this.converter();
            this.localizar.achados.clear();
            while (this.localizar.achados.size() != this.quant) {
                this.localizar.verificar();

            }
            this.JTQntGerados.setText("Quantos Pokemons foram gerados: " + this.localizar.achados.size());
            this.atualizarTabela();
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    public void converter() {
        try {
            this.quant = Integer.parseInt(this.jTextQuantidade.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite um numero.");
        }
    }

