    public void atualizarTabela() {
        DefaultTableModel model;

        model = (DefaultTableModel) this.jTable2.getModel();
        while (model.getRowCount() > 0) {

            model.removeRow(0);
        }
        for (int i = 0; i < this.localizar.achados.size(); i++) {

            model.addRow(new Object[]{this.localizar.achados.get(i).getIndece(), this.localizar.achados.get(i).getNome(),
                this.localizar.achados.get(i).getTipo().toString(), this.localizar.achados.get(i).getCordenada().getX(), this.localizar.achados.get(i).getCordenada().getY(), this.localizar.achados.get(i).getCordenada().getZ()});

        }
        this.jTextQuantidade.setText("");
    }

