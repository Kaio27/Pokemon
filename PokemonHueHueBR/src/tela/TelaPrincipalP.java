    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        clik++;
        this.linha = this.jTable2.getSelectedRow();
        long lDateTime = new Date().getTime();
        this.setSeed(lDateTime);
        this.ger.setSeed(this.getSeed());
        this.tentativas = ger.nextInt(2) + 4;
        if (clik < 2) {
            this.tenta = 0;
        } else {
            this.mostrar();
            clik = 0;
        }

    }//GEN-LAST:event_jTable2MouseClicked

    public void mostrar() {
        TelaMostrar mostra = new TelaMostrar();
        mostra.setMostra(this.localizar.achados.get(linha));
        mostra.setVisible(true);
        mostra.lerImagem();
    }

    private void jBCapturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCapturaActionPerformed
        // TODO add your handling code here:
        if (clik > 0) {
            long lDateTime = new Date().getTime();
            this.setSeed(lDateTime);
            ger.setSeed(this.getSeed());
            cap = ger.nextInt(6);
            if (this.tenta <= this.tentativas) {
                if (cap < this.tenta) {
                    JOptionPane.showMessageDialog(null, "Pokemon capturado.");
                    mostrar();
                    this.capturados.add(this.localizar.achados.get(linha));
                    this.localizar.achados.remove(linha);
                    this.clik = 0;
                    this.atualizarTabela();
                } else {
                    JOptionPane.showMessageDialog(null, "Pokemon escapou da pokebola.");
                }
                this.tenta++;
            } else {
                JOptionPane.showMessageDialog(null, "Pokemon escapou da pokebola.");
                JOptionPane.showMessageDialog(null, "Pokemon escapou.");
                this.localizar.achados.remove(linha);
                this.clik = 0;
                this.atualizarTabela();

            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Clique em alguma linha. ");
        }
    }//GEN-LAST:event_jBCapturaActionPerformed

